private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);
        for(EmployeeProfile ep:emphistory.getEmphistory()){
            Object[] rows = new Object[3];
            rows[0]= ep;
            rows[1]=ep.getEmpID();
            rows[2]= ep.getTitle();
            
            model.addRow(rows);
    }
    }

    private void populateSearch(ArrayList<EmployeeProfile> result) {
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);
        for(EmployeeProfile ep:result){
            Object[] rows = new Object[3];
            rows[0]= ep;
            rows[1]=ep.getEmpID();
            rows[2]= ep.getTitle();
            
            model.addRow(rows);
        }
    }
