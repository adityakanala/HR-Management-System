    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblView.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please Select an Employee to View full Details");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        EmployeeProfile selectedemp=(EmployeeProfile) model.getValueAt(selectedRowIndex, 0);

        txtName.setText(selectedemp.getName());
        txtEmpID.setText(String.valueOf(selectedemp.getEmpID()));
        txtAge.setText(String.valueOf(selectedemp.getAge()));
        txtGender.setText(selectedemp.getGender());
        datechooser.setDate(selectedemp.getDate());
        txtLevel.setText(String.valueOf(selectedemp.getLevel()));
        txtTeamInfo.setText(selectedemp.getTeam_Info());
        txtTitle.setText(selectedemp.getTitle());
        txtPhNo.setText(String.valueOf(selectedemp.getPhNo()));
        txtEmail.setText(selectedemp.getEmail());
        //lblPhoto.setIcon((Icon) selectedemp.getPhoto());
        
        ImageIcon ii= new ImageIcon(selectedemp.getPhoto());
            //Resize
        Image image=ii.getImage().getScaledInstance(lblPhoto.getWidth(), lblPhoto.getHeight(), Image.SCALE_SMOOTH);
        lblPhoto.setIcon(new ImageIcon(image));
        

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblView.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please Select an Employee to Delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        EmployeeProfile selectedemp=(EmployeeProfile) model.getValueAt(selectedRowIndex, 0);

        emphistory.deleteEmployee(selectedemp);
        JOptionPane.showMessageDialog(this,"Employee Deleted");
        populateTable();

        txtName.setText("");
        txtEmpID.setText("");
        txtAge.setText("");
        txtGender.setText("");
        datechooser.setDate(null);
        txtLevel.setText("");
        txtTeamInfo.setText("");
        txtTitle.setText("");
        txtPhNo.setText("");
        txtEmail.setText("");
        //lblPhoto.setIcon("");
    }//GEN-LAST:event_btnDeleteActionPerformed

